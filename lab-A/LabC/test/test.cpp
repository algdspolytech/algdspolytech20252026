TEST(MazeTest, create_queue_returnValidVal_no1) {
    queue q;
    create_queue(&q);

    EXPECT_EQ(q.start, 0);
    EXPECT_EQ(q.end, 0);
    EXPECT_EQ(q.filled, 0);
}

TEST(MazeTest, add_to_queue_returnValidVal_no2) {
    queue q;
    create_queue(&q);
    point p = { 1, 2 };

    add_to_queue(&q, p); 

    EXPECT_EQ(q.filled, 1);
    EXPECT_EQ(q.elems[0].x, 1);
    EXPECT_EQ(q.elems[0].y, 2);
}

TEST(MazeTest, get_from_queue_returnValidVal_no3) {
    queue q;
    create_queue(&q);
    point p_in = { 5, 6 };
    add_to_queue(&q, p_in);

    point p_out = get_from_queue(&q);

    EXPECT_EQ(p_out.x, 5);
    EXPECT_EQ(p_out.y, 6);
    EXPECT_EQ(q.filled, 0);
}

